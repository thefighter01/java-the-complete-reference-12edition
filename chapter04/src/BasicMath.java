public class BasicMath {
    public static void main(String[] args) {
        System.out.println("Integer Arithmetic");
        int a = 1 + 1;
        int b = a * 3;
        int c = b / 4;
        int d = c-a;
        int e = -d;
        System.out.println("theValueOf A is " + a);
        System.out.println("theValueOf B is " + b);
        System.out.println("theValueOf C is " + c);
        System.out.println("theValueOf D is " + d);
        System.out.println("theValueOf E is " + e);

        System.out.println("floating Point Arithmetic");
        double da = 1 + 1;
        double db = da * 3;
        double dc = db / 4;
        double dd = dc - a;
        double de = -dd;
        System.out.println("theValueOf dA is " + da);
        System.out.println("theValueOf dB is " + db);
        System.out.println("theValueOf dC is " + dc);
        System.out.println("theValueOf dD is " + dd);
        System.out.println("TheValueOF dE  "+ de);
    }
}
